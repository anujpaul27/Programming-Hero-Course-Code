const ar = [10,8,5,21,25,15,6]

// push value end of the array 
ar.push(50)
// console.log(...ar);
// delete last element of the array 
ar.pop()

// (pop_front) Delete first element of the array
ar.shift()
// (push_front) push first index of the array
ar.unshift(1)
console.log(ar);

// check array result is true & false
console.log(Array.isArray(ar));



